const TRANSLATIONS: Record<LangCode, typeof TR> = { tr: TR, en: EN, ru: RU, ar: AR };

export function detectLang(): LangCode {
  const code = window.Telegram?.WebApp?.initDataUnsafe?.user?.language_code ?? 'tr';
  if (code.startsWith('ru')) return 'ru';
  if (code.startsWith('ar')) return 'ar';
  if (code.startsWith('en')) return 'en';
  return 'tr';
}

export type Translations = typeof TR;
export type TranslationKey = keyof Translations;

let _t: typeof TR = TR;

export function initI18n() {
  _t = TRANSLATIONS[detectLang()] ?? TR;
}

export function t(key: TranslationKey): string {
  return _t[key] ?? TR[key] ?? key;
}

export function useT() {
  return t;
}
